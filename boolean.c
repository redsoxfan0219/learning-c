#include <stdio.h>
#include <stdbool.h>

int main() {

    bool value1 = true;
    bool value2 = false;

    printf("\nvalue1: %d\n", value1);
    printf("\nvalue2: %d\n\n", value2);

    // Logical Operators

    int x = 4;
    int y = 6;

    printf("\nIs x greater than y?   %d\n", x>y);
    printf("\nIs x less than y?   %d\n", x<y);
    printf("\nIs x equal to y?   %d\n\n", x==y);

}